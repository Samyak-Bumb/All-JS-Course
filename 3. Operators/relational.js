///  OR ///
// Relational Operators

i = 14;
j = 7;

// Greater than
a = (i > j)
console.log(a);

// Less than
a = (i < j)
console.log(a);

// Greater than or Equal's To
a = (i >= j)
console.log(a);

// Less than
a = (i <= j)
console.log(a);

// Equal's Equal's To
a = (i == j)
console.log(a);

// Not Equal's To
a = (i != j)
console.log(a);
