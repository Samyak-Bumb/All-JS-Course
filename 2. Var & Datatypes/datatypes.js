// Number type :-

// Integer
var a = 69;
// Floating
var b = 6.9;
console.log(a, typeof a);
console.log(b, typeof b);
// Infinity (Any no. divided by 0)
var d = 4 / 0;
console.log(d, typeof d);
console.log("---");

// String type :-

// Double Quote
var c = "Samyak";
// Single Quote
var str = 'SamyAK';
console.log(c, typeof c);
console.log(str, typeof str);
console.log("---");

// Boolean type :- ("True" OR "False")
let alien = true;
let human = false;
console.log(alien, typeof alien);
console.log(human, typeof human);
console.log("---");

// null :- ("nothing", "empty", "unknown")
var z = null;
console.log(z, typeof z);
console.log("---");

// undefined (Var is declared but not assigned)
var e;
console.log(e, typeof e);
